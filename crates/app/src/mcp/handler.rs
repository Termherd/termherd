//! The MCP tool surface exposed to hosted Claude sessions.
//!
//! [`TermherdMcp`] is an rmcp [`ServerHandler`]: it answers `tools/list` and
//! `tools/call` for the live-bridge tools. Each tool is a thin adapter — it
//! translates the call into a [`Request`] on the async-transport bridge, awaits
//! the timeout-bounded [`Reply`], and shapes it back into an MCP result. No state
//! and no `core` access live here; the shell owns `core::App` and answers on the
//! bridge, so a tool can never touch it directly.

use std::time::Duration;

use std::collections::BTreeMap;

use rmcp::{
    ErrorData, ServerHandler,
    handler::server::{router::tool::ToolRouter, wrapper::Parameters},
    model::{CallToolResult, Implementation, ProtocolVersion, ServerCapabilities, ServerInfo},
    schemars::JsonSchema,
    tool, tool_handler, tool_router,
};
use serde::{Deserialize, Serialize};
use termherd_core::workspace::SplitDir;
use termherd_core::{Section, SessionStatus, SnapshotFilter, TerminalScope, WorkspaceSnapshot};

use crate::shell::bridge::{Action, BridgeHandle, Reply, Request, SessionInfo, SessionKind};

/// How long a tool waits for the shell to answer before failing the caller.
/// Bounds the whole round-trip (enqueue + reply) via [`BridgeHandle::call`], so
/// a wedged shell surfaces as a tool error, never a hang (Q7).
const CALL_TIMEOUT: Duration = Duration::from_secs(5);

/// The live-bridge MCP server handler. Holds only the bridge into the shell —
/// cloned fresh per session by the transport's service factory.
#[derive(Clone)]
pub struct TermherdMcp {
    bridge: BridgeHandle,
    tool_router: ToolRouter<Self>,
}

#[tool_router(router = tool_router)]
impl TermherdMcp {
    /// Build a handler over `bridge`. The tool router is generated by the
    /// `#[tool_router]` macro from the `#[tool]` methods below.
    pub fn new(bridge: BridgeHandle) -> Self {
        Self {
            bridge,
            tool_router: Self::tool_router(),
        }
    }

    /// The spike tool: list the live terminal sessions termherd is hosting, each
    /// with its stable handle, so an MCP client can address them.
    #[tool(
        name = "list_sessions",
        description = "List the live terminal sessions termherd is hosting. Each \
                       row carries a stable `handle` (address it in later calls), \
                       the tab title, working directory, kind (shell or claude), \
                       the resumed Claude id if any, and current activity status."
    )]
    async fn list_sessions(&self) -> Result<CallToolResult, ErrorData> {
        let reply = self
            .bridge
            .call(Request::ListSessions, CALL_TIMEOUT)
            .await
            .map_err(|error| ErrorData::internal_error(error.to_string(), None))?;
        let Reply::Sessions(sessions) = reply else {
            return Err(ErrorData::internal_error(
                "bridge answered the wrong reply kind",
                None,
            ));
        };
        let rows: Vec<SessionDto> = sessions.iter().map(SessionDto::from).collect();
        let value = serde_json::to_value(&rows)
            .map_err(|error| ErrorData::internal_error(error.to_string(), None))?;
        Ok(CallToolResult::structured(value))
    }

    /// A filterable, read-only snapshot of the whole workspace — the structured
    /// "DOM" a client reads to perceive termherd before acting. Light by default
    /// (structure only, no terminal text); scope terminal text to named handles.
    #[tool(
        name = "snapshot",
        description = "A filterable snapshot of termherd's whole state: config, \
                       the session-browser sidebar, and the open tabs with their \
                       panes (each pane's stable handle, kind, cwd, status). Light \
                       by default — no terminal text. Args (all optional): \
                       `sections` (any of \"config\", \"sidebar\", \"tabs\"; omit \
                       for all), `terminals` (session handles to include screen \
                       text for; omit for none — read the structure first, then \
                       request a handle), `text_lines` (trailing lines per \
                       requested terminal, default 40)."
    )]
    async fn snapshot(
        &self,
        Parameters(args): Parameters<SnapshotArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let reply = self
            .bridge
            .call(Request::Snapshot(args.into_filter()), CALL_TIMEOUT)
            .await
            .map_err(|error| ErrorData::internal_error(error.to_string(), None))?;
        let Reply::Snapshot(snapshot) = reply else {
            return Err(ErrorData::internal_error(
                "bridge answered the wrong reply kind",
                None,
            ));
        };
        let value = serde_json::to_value(SnapshotDto::from(&snapshot))
            .map_err(|error| ErrorData::internal_error(error.to_string(), None))?;
        Ok(CallToolResult::structured(value))
    }

    /// Open a new terminal session and focus it. → `open_session`.
    #[tool(
        name = "open_session",
        description = "Open a new terminal session and focus it. Args: `project` \
                       (working directory; omit for the home dir), `kind` \
                       (\"shell\" or \"claude\", default \"shell\"). Returns the \
                       new session's `focused_handle`."
    )]
    async fn open_session(
        &self,
        Parameters(args): Parameters<OpenArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let action = Action::Open {
            project: args.project,
            kind: match args.kind.as_deref() {
                None | Some("shell") => SessionKind::Shell,
                Some("claude") => SessionKind::Claude,
                Some(other) => {
                    return Err(ErrorData::invalid_params(
                        format!("unknown kind {other:?}; expected \"shell\" or \"claude\""),
                        None,
                    ));
                }
            },
        };
        self.act(action).await
    }

    /// Split a pane, opening a fresh session beside it. → `split_pane`.
    #[tool(
        name = "split_pane",
        description = "Split a pane, opening a fresh shell beside it and focusing \
                       it. Args: `direction` (\"vertical\" = side by side, \
                       \"horizontal\" = stacked; default \"vertical\"), `pane` \
                       (session handle to split; omit for the focused pane). \
                       Returns the new pane's `focused_handle`."
    )]
    async fn split_pane(
        &self,
        Parameters(args): Parameters<SplitArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let dir = match args.direction.as_deref() {
            None | Some("vertical") | Some("v") => SplitDir::Vertical,
            Some("horizontal") | Some("h") => SplitDir::Horizontal,
            Some(other) => {
                return Err(ErrorData::invalid_params(
                    format!("unknown direction {other:?}; expected \"vertical\" or \"horizontal\""),
                    None,
                ));
            }
        };
        let pane = parse_optional_handle(args.pane)?;
        self.act(Action::Split { pane, dir }).await
    }

    /// Move focus to the pane hosting a session. → `focus_pane`.
    #[tool(
        name = "focus_pane",
        description = "Move focus to the pane hosting a session, and hand it the \
                       keyboard. Args: `session` (its stable handle). Returns the \
                       resulting `focused_handle`."
    )]
    async fn focus_pane(
        &self,
        Parameters(args): Parameters<FocusArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let session = parse_handle(&args.session)?;
        self.act(Action::Focus { session }).await
    }

    /// Give a tab a manual name. → `rename_tab`.
    #[tool(
        name = "rename_tab",
        description = "Give the tab at `tab` (0-based index, as `snapshot` reports \
                       tab order) a manual name. A blank `title` reverts to the \
                       derived title. Returns the current `focused_handle`."
    )]
    async fn rename_tab(
        &self,
        Parameters(args): Parameters<RenameArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        self.act(Action::Rename {
            tab: args.tab,
            title: args.title,
        })
        .await
    }

    /// Close a pane; a lone pane closes its whole tab. → `close_pane`.
    #[tool(
        name = "close_pane",
        description = "Close a pane and kill its terminal. Args: `pane` (session \
                       handle to close; omit for the focused pane). A lone pane is \
                       its whole tab, which closes. Returns the resulting \
                       `focused_handle` (null when the workspace is now empty)."
    )]
    async fn close_pane(
        &self,
        Parameters(args): Parameters<CloseArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let pane = parse_optional_handle(args.pane)?;
        self.act(Action::Close { pane }).await
    }

    /// Type text into a session's terminal without waiting. → `run_in_session`.
    #[tool(
        name = "run_in_session",
        description = "Type text into a session's terminal, as if typed at the \
                       keyboard — does not wait for it to finish (read the result \
                       with `snapshot`'s scoped terminal text). Include a trailing \
                       newline in `text` to submit a command. Args: `session` (its \
                       stable handle), `text`. Returns the `focused_handle`."
    )]
    async fn run_in_session(
        &self,
        Parameters(args): Parameters<RunArgs>,
    ) -> Result<CallToolResult, ErrorData> {
        let session = parse_handle(&args.session)?;
        self.act(Action::Run {
            session,
            bytes: args.text.into_bytes(),
        })
        .await
    }
}

impl TermherdMcp {
    /// Send an [`Action`] over the bridge and shape its [`ActionOutcome`] into a
    /// tool result: a rejection (unknown handle / out-of-range tab) becomes an
    /// `invalid_params` error, an applied action a `{ focused_handle }` object.
    async fn act(&self, action: Action) -> Result<CallToolResult, ErrorData> {
        let reply = self
            .bridge
            .call(Request::Act(action), CALL_TIMEOUT)
            .await
            .map_err(|error| ErrorData::internal_error(error.to_string(), None))?;
        let Reply::Acted(outcome) = reply else {
            return Err(ErrorData::internal_error(
                "bridge answered the wrong reply kind",
                None,
            ));
        };
        if let Some(reason) = outcome.error {
            return Err(ErrorData::invalid_params(reason, None));
        }
        let value = serde_json::json!({ "focused_handle": outcome.focused });
        Ok(CallToolResult::structured(value))
    }
}

#[tool_handler(router = self.tool_router)]
impl ServerHandler for TermherdMcp {
    fn get_info(&self) -> ServerInfo {
        // rmcp's `ServerInfo`/`Implementation` are `#[non_exhaustive]`, so build
        // from a default and set the fields we care about rather than a literal.
        let mut server = Implementation::from_build_env();
        server.name = "termherd".into();
        server.version = env!("CARGO_PKG_VERSION").into();

        let mut info = ServerInfo::default();
        info.protocol_version = ProtocolVersion::LATEST;
        info.capabilities = ServerCapabilities::builder().enable_tools().build();
        info.server_info = server;
        info.instructions = Some(
            "termherd's live bridge: read and (later) drive the terminal \
             sessions it is hosting."
                .into(),
        );
        info
    }
}

/// The on-the-wire shape of a session for the `list_sessions` tool: the stable
/// external handle plus flat, string-typed fields an MCP client can read without
/// knowing termherd's internal enums.
#[derive(Serialize)]
struct SessionDto {
    /// Stable external handle (the runtime session id).
    handle: String,
    title: String,
    cwd: Option<String>,
    /// `"shell"` or `"claude"`.
    kind: &'static str,
    /// The resumed Claude session id, if any (the *unstable* id — never the
    /// handle).
    resume_id: Option<String>,
    /// `"starting"`, `"busy"`, `"idle"`, `"attention"`, or `"exited"`.
    status: &'static str,
}

impl From<&SessionInfo> for SessionDto {
    fn from(info: &SessionInfo) -> Self {
        Self {
            handle: info.handle.clone(),
            title: info.title.clone(),
            cwd: info.cwd.clone(),
            kind: match info.kind {
                SessionKind::Shell => "shell",
                SessionKind::Claude => "claude",
            },
            resume_id: info.resume_id.clone(),
            status: status_str(info.status),
        }
    }
}

/// The `snapshot` tool's arguments — every field optional so a bare call is the
/// light default. Deserialised from the MCP `arguments` object.
#[derive(Debug, Default, Deserialize, JsonSchema)]
#[schemars(crate = "rmcp::schemars")]
struct SnapshotArgs {
    /// Sections to include: any of `"config"`, `"sidebar"`, `"tabs"`. Omit for
    /// all three. Unknown names are ignored.
    #[serde(default)]
    sections: Option<Vec<String>>,
    /// Session handles (as strings, as `list_sessions` reports them) to include
    /// terminal text for. Omit (or empty) for none.
    #[serde(default)]
    terminals: Option<Vec<String>>,
    /// Include the focused pane's terminal text — a one-call shortcut when you
    /// don't yet know its handle. Ignored when `terminals` names handles.
    #[serde(default)]
    focused_terminal: Option<bool>,
    /// Trailing terminal lines to keep per requested session.
    #[serde(default)]
    text_lines: Option<usize>,
}

impl SnapshotArgs {
    /// Map the flat MCP arguments onto the core [`SnapshotFilter`]. Omitted
    /// fields fall back to the light default. Explicit handles win over the
    /// `focused_terminal` shortcut.
    fn into_filter(self) -> SnapshotFilter {
        let default = SnapshotFilter::default();
        let sections = match self.sections {
            Some(names) => names
                .iter()
                .filter_map(|name| match name.as_str() {
                    "config" => Some(Section::Config),
                    "sidebar" => Some(Section::Sidebar),
                    "tabs" => Some(Section::Tabs),
                    _ => None,
                })
                .collect(),
            None => default.sections,
        };
        let terminals = match self.terminals {
            Some(handles) if !handles.is_empty() => {
                TerminalScope::Only(handles.iter().filter_map(|h| h.parse().ok()).collect())
            }
            _ if self.focused_terminal == Some(true) => TerminalScope::Focused,
            _ => TerminalScope::None,
        };
        SnapshotFilter {
            sections,
            terminals,
            text_lines: self.text_lines.unwrap_or(default.text_lines),
        }
    }
}

